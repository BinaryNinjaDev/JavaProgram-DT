 class StringDemo12 {

	public static void main(String[] args) {
		char ch = 'n';
		
		String s1 = String.valueOf(ch);
		System.out.println(s1);
		System.out.println(s1.length());
		
		boolean b = true;
		String s2 = String.valueOf(b);
		
		System.out.println(s2);
		System.out.println(s2.length());

	}

}
